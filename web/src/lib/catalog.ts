export function getProductBySlug(slug: string): Product | null {
  const p = PRODUCTS.find((x) => x.slug === slug);
  if (p) return p;
  const m = MIN_PRODUCTS.find((x) => x.slug === slug);
  return m ? toProduct(m) : null;
}
